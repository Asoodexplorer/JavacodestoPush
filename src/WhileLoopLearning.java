public class WhileLoopLearning {

    public static void main(String[] args) {
        System.out.println( "Odd number of this series are : ");

        int i =10;

        while (i>=1) { //here i is less than 9,when it will go above 9, condition will become false
            System.out.println (i); // this will print value of i
           --i; // this will increase the value by 2 every time

        }
        }

    }



